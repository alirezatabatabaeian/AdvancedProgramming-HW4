template <typename T>
SharedPtr<T>::SharedPtr(T* ptr)
    : _p { ptr }
{
}
////////////////////////////////////////////////
template <typename T>
SharedPtr<T>::SharedPtr()
    : _p { nullptr }
{
}
////////////////////////////////////////////////
template <typename T>
SharedPtr<T>::~SharedPtr()
{
    delete _p;
    _p = nullptr;
}
